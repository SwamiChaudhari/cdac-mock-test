import json
from collections import Counter

cards = []
n = [0]  # mutable counter

def add(subject, topic, diff, imp, eq, expl, why, vis, real, formula, trick, mistakes, qs, revision):
    n[0] += 1
    cards.append({
        "id": f"tc_{n[0]:04d}",
        "topicNumber": n[0],
        "topicName": topic,
        "subject": subject,
        "difficulty": diff,
        "importance": imp,
        "expectedQuestions": eq,
        "simpleExplanation": expl,
        "whyItMatters": why,
        "visualDiagram": vis,
        "realWorldExample": real,
        "formula": formula,
        "memoryTrick": trick,
        "commonMistakes": mistakes,
        "topQuestions": qs,
        "quickRevision": revision,
        "masteryCheck": []
    })

def _mq(q_text, opts, ans, diff, freq, concept, hint, explanation, shortcut, trap, memory):
    """Build a question dict with all required fields for the enhanced format"""
    return {
        "question": q_text,
        "options": list(opts),
        "correctAnswer": ans,
        "difficulty": diff,
        "examFrequency": freq,
        "conceptTested": concept,
        "hint": hint,
        "detailedExplanation": explanation,
        "shortcutMethod": shortcut,
        "commonTrap": trap,
        "memoryTrick": memory
    }


